public class OperacaoAritimetica {
    public static void main(String[] args) throws Exception{
        int PrimeiroNumero = 6;
        int SegundoNumero = 8;

        int Soma = PrimeiroNumero + SegundoNumero;
        int Subtracao = SegundoNumero - PrimeiroNumero;
        int Multiplicacao = PrimeiroNumero * SegundoNumero;
        float Divisao = SegundoNumero / PrimeiroNumero;

        System.out.println("A soma de 6 + 8 = " + Soma);
        System.out.println("A subtração de 8 - 6 = " + Subtracao);
        System.out.println("A multiplicação de  8 * 6 = " + Multiplicacao);
        System.out.println("A divisão de 8 / 6 = " + Divisao);


    }
    
}
